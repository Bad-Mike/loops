public class loop1 {

    public static void main(String[] args) {

        // declare x
        int x;
        x = 0;

        while ( x < 5) {        //while loop - control x is smaller than 5

            x++;                //count plus 1
            System.out.println(x);
        }                       //end loop - till 5

        System.out.println("end loop1");
    }
}
