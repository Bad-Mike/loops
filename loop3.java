public class loop3 {

    public static void main(String[] args) {

        for (int x = 0; x < 5; x++) {       //for loop, declare in the loop, declaration only for the loop

            System.out.println(x + 1);      //+ 1 to count form 1 to 5
        }

        System.out.println("end loop3");
    }
}
